  constructor() {
    // Load language from localStorage
    const savedLang = localStorage.getItem('language') as Language;
    if (savedLang && (savedLang === 'en' || savedLang === 'he')) {
      this.currentLanguage$.next(savedLang);
    }
  }

  get language$(): Observable<Language> {
    return this.currentLanguage$.asObservable();
  }

  get currentLanguage(): Language {
    return this.currentLanguage$.value;
  }

  setLanguage(lang: Language): void {
    this.currentLanguage$.next(lang);
    localStorage.setItem('language', lang);

    // Update document direction
    document.documentElement.dir = lang === 'he' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
  }

  translate(key: string, params?: { [key: string]: any }): string {
    const lang = this.currentLanguage;
    let translation = this.translations[key]?.[lang] || key;

    // Replace parameters if provided
    if (params) {
      Object.keys(params).forEach(paramKey => {
        translation = translation.replace(`{${paramKey}}`, params[paramKey]);
      });
    }

    return translation;
  }

  // Shorthand alias
  t(key: string, params?: { [key: string]: any }): string {
    return this.translate(key, params);
  }

  // Translate Supabase error messages
  translateError(errorMessage: string): string {
    const errorMap: { [key: string]: string } = {
      'Invalid login credentials': 'auth.invalidCredentials',
      'Invalid email': 'auth.invalidEmail',
      'Email not confirmed': 'auth.emailNotConfirmed',
      'User not found': 'auth.userNotFound',
      'User already registered': 'auth.emailAlreadyExists',
      'Password should be at least 6 characters': 'auth.weakPassword',
      'Too many requests': 'auth.tooManyRequests',
    };

    // Check if error message matches known patterns
    for (const [pattern, key] of Object.entries(errorMap)) {
      if (errorMessage.includes(pattern)) {
        return this.t(key);
      }
    }

    // Return original message if no translation found
    return errorMessage;
  }
